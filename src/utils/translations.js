// Función para detectar idioma del navegador
export const detectLanguage = () => {
  // 1. Verificar si hay idioma guardado en localStorage
  const savedLanguage = localStorage.getItem('blackjack-language');
  if (savedLanguage && translations[savedLanguage]) {
    return savedLanguage;
  }
  
  // 2. Detectar idioma del navegador
  const browserLanguages = [
    navigator.language,
    navigator.userLanguage,
    ...(navigator.languages || [])
  ];
  
  for (const browserLang of browserLanguages) {
    if (!browserLang) continue;
    
    // Buscar mapeo exacto
    if (languageMapping[browserLang]) {
      return languageMapping[browserLang];
    }
    
    // Buscar por código base (ej: 'es' de 'es-MX')
    const baseCode = browserLang.split('-')[0];
    if (translations[baseCode]) {
      return baseCode;
    }
  }
  
  // 3. Fallback a inglés por defecto
  return 'en';
};

// Función para cambiar idioma
export const setLanguage = (languageCode) => {
  if (translations[languageCode]) {
    localStorage.setItem('blackjack-language', languageCode);
    return true;
  }
  return false;
};

// Función para obtener traducción
export const t = (key, language = null) => {
  const currentLanguage = language || detectLanguage();
  const translation = translations[currentLanguage];
  
  if (!translation) {
    console.warn(`No translation found for language: ${currentLanguage}`);
    return translations.en[key] || key;
  }
  
  return translation[key] || translations.en[key] || key;
};

// Función para obtener lista de idiomas disponibles
export const getAvailableLanguages = () => {
  return Object.keys(translations).map(code => ({
    code,
    ...languageInfo[code]
  }));
};
