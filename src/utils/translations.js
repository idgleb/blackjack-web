// FunciÃ³n para detectar idioma del navegador
export const detectLanguage = () => {
  // 1. Verificar si hay idioma guardado en localStorage
  const savedLanguage = localStorage.getItem('blackjack-language');
  if (savedLanguage && translations[savedLanguage]) {
    return savedLanguage;
  }
  
  // 2. Detectar idioma del navegador
  const browserLanguages = [
    navigator.language,
    navigator.userLanguage,
    ...(navigator.languages || [])
  ];
  
  for (const browserLang of browserLanguages) {
    if (!browserLang) continue;
    
    // Buscar mapeo exacto
    if (languageMapping[browserLang]) {
      return languageMapping[browserLang];
    }
    
    // Buscar por cÃ³digo base (ej: 'es' de 'es-MX')
    const baseCode = browserLang.split('-')[0];
    if (translations[baseCode]) {
      return baseCode;
    }
  }
  
  // 3. Fallback a inglÃ©s por defecto
  return 'en';
};

// FunciÃ³n para cambiar idioma
export const setLanguage = (languageCode) => {
  if (translations[languageCode]) {
    localStorage.setItem('blackjack-language', languageCode);
    return true;
  }
  return false;
};

